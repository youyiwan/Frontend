DROP TABLE IF EXISTS CUSTOMER

create table CUSTOMER (
	CUSTOMER_ID_1 VARCHAR(64) NOT NULL,
    FIRST_NAME_1 VARCHAR(64) NOT NULL,
	LAST_NAME_1 VARCHAR(64) NOT NULL,
	CUSTOMER_ID_2 VARCHAR(64),
    FIRST_NAME_2 VARCHAR(64),
	LAST_NAME_2 VARCHAR(64),
	PRIMARY_ADDRESS VARCHAR(64) NOT NULL,
	PIMARY_CONTACT_NO INT NOT NULL,
	PIMARY_EMAIL VARCHAR(64) NOT NULL,
	
	PRIMARY KEY(customer_id_1, customer_id_2)
);

DROP TABLE IF EXISTS ACCOUNT 


create table ACCOUNT (
	ACCOUNT_ID VARCHAR(64), 
    TRANS_ID VARCHAR(64),
	CUSTOMER_ID_1  VARCHAR(64) ,
	CUSTOMER_ID_2 VARCHAR(64) ,
	DEBIT REAL, 
	CREDIT REAL,
	BALANCE VARCHAR(64),
	TRANS_DATE DATE,

	PRIMARY KEY(ACCOUNT_ID, TRANS_ID)
);

